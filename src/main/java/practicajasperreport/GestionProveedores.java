    private void prepararOperacion(TiposOperacion tipo) {
        tipoOperacion = tipo;
        borrarCampos(getCampos());
        cambiarColorCampos(getCampos(), Color.WHITE);
        setEnabledFields(getCampos(), codigo, false);
        codigo.setEnabled(true);
        codigo.grabFocus();
        botonAceptar.setEnabled(false); 
    }
    
    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed

        boolean exito = false;
        switch (tipoOperacion) {
            case ALTA -> {
                if (validarCamposAlta() && calcularLetraDni() && validarCodigoPostal()) {
                    insertarProveedor();
                    exito = true;
                }
            }
            case BAJA -> {
                borrarProveedor();
                exito = true;
            }
            case MODIFICAR -> {
                if (calcularLetraDni() && validarCodigoPostal()) {
                    modificarProveedor();
                    exito = true;
                }
            }
            default -> {
                JOptionPane.showMessageDialog(this,
                    "No hay ninguna operación seleccionada", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        
        if (exito) {
            borrarCampos(getCampos());
            cambiarColorCampos(getCampos(), Color.WHITE);
            setEnabledFields(getCampos(), null, false);
            codigo.setEnabled(false);
            tipoOperacion = TiposOperacion.NO_SELECCIONADO;
        }
    
    }//GEN-LAST:event_botonAceptarActionPerformed

