    private boolean checkProveedorId() {
        try {
            Statement st = con.createStatement();
             int cod = Integer.parseInt(codigo.getText());

            ResultSet rs = st.executeQuery("SELECT codigo FROM proveedores WHERE codigo = " + cod);
            
            if (rs.next()) {
                return true;
            } 
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showConfirmDialog(this, 
                    "Error de consulta con la BBDD", "Error en la consulta", JOptionPane.CLOSED_OPTION);
        }
        
        return false;
    }

    private void cargarProveedorPorCodigo() {

        String sql = "SELECT codigo, dni, apellidos, nombre, direccion, " +
                     "codigo_postal, localidad, telefono, movil, fax, email, total " +
                     "FROM proveedores WHERE codigo = " + codigo.getText();

        try (Statement st = con.createStatement();
             ResultSet rs = st.executeQuery(sql)) {

            if (rs.next()) {
                String fullDni = rs.getString("dni");
                if(fullDni != null && fullDni.length() >= 8) {
                    dni.setText(fullDni.substring(0, fullDni.length()-1));
                    letraDni.setText(fullDni.substring(fullDni.length()-1));
                }

                apellidos.setText(rs.getString("apellidos"));
                nombre.setText(rs.getString("nombre"));
                direccion.setText(rs.getString("direccion"));
                codigoPostal.setText(rs.getString("codigo_postal"));
                localidad.setText(rs.getString("localidad"));
                
 
                telefono.setText(rs.getString("telefono") != null ? rs.getString("telefono") : "");
                movil.setText(rs.getString("movil") != null ? rs.getString("movil") : "");
                fax.setText(rs.getString("fax") != null ? rs.getString("fax") : "");
                email.setText(rs.getString("email") != null ? rs.getString("email") : "");
                
                total.setText(String.valueOf(rs.getFloat("total")));
            } 

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Error al cargar el proveedor",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }
    
