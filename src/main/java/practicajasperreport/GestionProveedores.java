    private void codigoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_codigoKeyTyped
        
        char c = evt.getKeyChar();
        if (!Character.isDigit(c)) {
            evt.consume();
        }
        
         if (codigo.getText().length() >= 5 ) {
            evt.consume();
        }
    }//GEN-LAST:event_codigoKeyTyped

    private void codigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_codigoKeyPressed
       
        if (evt.getKeyCode() == VK_ENTER && codigo.getText().length() == 5) {
            
           switch (tipoOperacion) {
                case ALTA -> {
                    if (checkProveedorId()) {
                        JOptionPane.showMessageDialog(this,
                            "El proveedor ya existe",
                            "Error",
                            JOptionPane.ERROR_MESSAGE);
                        codigo.setText("");
                        codigo.grabFocus();
                    } else {
                        setEnabledFields(getCampos(), codigo, true);
                        codigo.setEnabled(false);
                        total.setText("0");
                        total.setEnabled(false);
                        botonAceptar.setEnabled(true);
                        dni.grabFocus();
                    }
                }
                case BAJA, MODIFICAR, CONSULTA -> {
                    if (!checkProveedorId()) {
                        JOptionPane.showMessageDialog(this, "El proveedor no existe", "Error", JOptionPane.ERROR_MESSAGE);
                        codigo.setText("");
                        codigo.grabFocus();
                    } else {
                        cargarProveedorPorCodigo();
                        
                        if (tipoOperacion == TiposOperacion.MODIFICAR) {
                            setEnabledFields(getCampos(), codigo, true); 
                            codigo.setEnabled(false);
                            total.setEnabled(false); 
                            botonAceptar.setEnabled(true);
                            dni.grabFocus();
                        } else {
                            // Baja o Consulta
                            setEnabledFields(getCampos(), null, false);
                            if (tipoOperacion == TiposOperacion.BAJA) {
                                botonAceptar.setEnabled(true); 
                            } else {
                                botonAceptar.setEnabled(false);     
                            }
                        }
                    }
                }
        
                default -> System.out.println("No se ha insertado un código correcto.");
           }
        } 
    }//GEN-LAST:event_codigoKeyPressed

